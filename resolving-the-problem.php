    <h3 style="font-weight: 600; font-style: italic;">Challenges to Nomination Papers</h3>

    <p>
        With 40,000 elected offices in Illinois, in the limited time available between filing nomination papers and
        needing to prepare the ballot, no court or officials could possibly validate all the signatures and other
        requirements of nomination papers. Illinois law assumes that the other candidates (or their friends) will
        examine the papers of their opponents and bring to the attention of the authorities any defects discovered.
        It is a valuable process that weeds out candidates who cannot follow the law's simple directions in
        preparing their own important papers from being elected to a position where they must follow detailed
        directions set by law in spending taxpayer money or administering important functions to their residents.
    </p>

    <p>
        That process is initiated by filing, within five business days after the close of the filing period, a
        written Objection with the office where the nomination papers were filed. It need not be in any specific
        form as long as it states the name and residence address of the objector, states what his interest is in
        filing the objection, states fully exactly what is wrong with the nomination papers, and what the objector
        wants the officials to do about it.
    </p>

    <p>
        The law creates an Electoral Board with automatic members to hear the case, often of local elected
        officials. Those persons are not experts in Illinois election law, and they often end up making up the rules
        as they go along, unaware of how other Electoral Boards or Courts have ruled on points raised that were not
        spelled out in detail in the written law. Sadly, many times something ruled against by one Electoral Board
        would have been acceptable to an Electoral Board elsewhere.
    </p>






