                    <br>
                    <p>
                        The Open Meetings Act gives anyone (regardless of where they live) the right to attend meetings
                        of public bodies, to be aware in advance of what will be discussed or voted upon, to address the
                        assembly, and to freely make audio or video recordings of the proceedings,
                    </p>

                    <p>
                        A majority of a quorum of a public body must not come together to discuss public business except
                        at a meeting advertised in advance and held at a time and in a place convenient to the public.
                        Any member of the public must be allowed to address the board on any topic during time reserved
                        for that. No final action can be taken on any matter that was not advertised on a agenda
                        available to the public 48 hours before the meeting. That applies to conference calls and
                        e-mails too.
                    </p>

                    <p>
                        There are some situations where the officials are allowed (but are never required) to go behind
                        closed doors, but those are legally limited to a small group of very specific instances (such as
                        setting strategy for negotiating property sales or union contracts or litigation). Any final
                        action must be done in the open.
                    </p>

                    <p>
                        Sometimes some officials misunderstand how constrained the permission is to hold a closed
                        meeting, despite having passed a mandated training course, and they violate the law by
                        discussing other matters. Some try to intimidate others by asserting (incorrectly) that it is
                        illegal to reveal anything said in a closed meeting.
                    </p>

                    <p>
                        Any person can sue when there has been, or probably will be, a violation, and if he
                        substantially prevails, the public body may have to pay his attorney's fees and court costs.
                    </p>

                    <p>
                        A public body found to have illegally discussed something in a closed meeting can be ordered to
                        make public the required audio recordings made of the meeting. Any final action taken there can
                        be nullified. Additionally, participants can be personally fined up to $1,500, and are even
                        subject to spending up to 30 days in the county jail.
                    </p>

                    <p>
                        Instead of suing, within 60 days after the violation, any person can request a review by the
                        <a href="http://foia.ilattorneygeneral.net/" class="link"> Public Access Counselor</a> in the
                        Attorney General's Office.
                    </p>
