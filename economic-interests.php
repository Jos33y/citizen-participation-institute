            <h3 class="resolve-two"> Economic Interests </h3>

            <p class="resolve-p">
                “All candidates for or holders of state offices and all members of a Commission or Board created by this
                Constitution shall file a verified statement of their economic interests,as provided by law. The General
                Assembly by law may impose a similar requirement upon candidates for, or holders of, offices in units of
                local government and school districts. Statements shall be filed annually ... and shall be available for
                inspection by the public. Failure to file a statement within the time prescribed shall result in
                ineligibility for, or forfeiture of, office.”
                <span style="font-style: normal; font-size: 65%;"> (Ill. Constitution, Art. XIII, Sec. 2)</span>
            </p>

            <p>
                The Governmental Ethics Act
                [<a href="http://www.ilga.gov/legislation/ilcs/ilcs4.asp?DocName=000504200HArt%2E+4A&ActID=129&ChapterID=2&SeqStart=5750000&SeqEnd=6700000"
                    target="_blank" class="link">5 ILCS 420/4A</a>] requires all elected officials, candidates, and
                certain
                public employees to annually file (usually before May 31 every year) a form that nominally is designed
                to disclose potential conflicts of interest. Anyone who willfully files a false or incomplete Statement
                of Economic Interests is subject to a fine of up to $2,500, and even a year in the county jail. For
                failure to file at all, the local State's Attorney is required by both the Illinois Constitution and the
                statute to have the official or employee removed from office.
            </p>

            <p>
                ​Statements of Economic Interest are usually filed with the county clerk of the county in which the
                principal office of that person's public body is located. Some public bodies stretch into more than one
                county. To determine which county handles the filings, click <a href="record-request.php"
                    target="_blank" class="link"> here</a> to look up that public body; the name
                of the relevant county appears below the name of the public body there.
            </p>

