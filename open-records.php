                    <br>
                    <p>
                        The Freedom of Information Act generally provides that anyone is entitled to inspect, copy, and
                        have a copy of any public record in the possession or under the control of any public body. That
                        includes emails or texts on an official's private computer or phone that relate to public
                        business.
                    </p>

                    <p>
                        There are some records where the public body is allowed to blank-out certain information, or
                        even withhold the entire record. Examples include a person's social security number, or autopsy
                        photos.
                    </p>

                    <p>
                        Sometimes public bodies interpret those limited exemptions too loosely. A requester may sue for
                        the records or information denied, and the public body will have to pay his attorney's fees and
                        court costs if he prevails. If the public body intentionally violated the law, or otherwise
                        operated in bad faith, it can also be fined $2,500 to $5,000 for each occurrence.
                    </p>

                    <p>
                        Instead of suing, within 60 days after the denial the requester can request a review by the
                        <a href="http://foia.ilattorneygeneral.net/" class="link"> Public Access Counselor</a> in the
                        Attorney General's Office. The Public Access Counselor can review
                        the situation and advise the public body to release improperly withheld information. Sometimes,
                        the Public Access Counselor can even compel the release.
                    </p>

